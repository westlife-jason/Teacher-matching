/**
 * 주소 → 좌표 변환
 * 1) 내장 룩업 테이블 우선 매칭 (빠르고 안정적)
 * 2) 테이블에 없으면 Nominatim API 폴백 (24시간 캐시)
 */
export async function geocodeAddress(
  address: string
): Promise<{ lat: number; lon: number } | null> {
  const normalized = address.trim();

  // 1. 완전 일치
  if (KR_COORDS[normalized]) return KR_COORDS[normalized];

  // 2. 접두어 부분 일치 (긴 것 우선)
  const keys = Object.keys(KR_COORDS).sort((a, b) => b.length - a.length);
  for (const key of keys) {
    if (normalized.startsWith(key) || normalized.includes(key)) {
      return KR_COORDS[key];
    }
  }

  // 3. Nominatim 폴백 (테이블에 없는 주소)
  try {
    const url = `https://nominatim.openstreetmap.org/search?q=${encodeURIComponent(normalized)}&format=json&limit=1&countrycodes=kr`;
    const res = await fetch(url, {
      headers: { "User-Agent": "teacher-matching-app/1.0" },
      next: { revalidate: 86400 },
    });
    if (!res.ok) return null;
    const data = await res.json() as Array<{ lat: string; lon: string }>;
    if (!data.length) return null;
    return { lat: parseFloat(data[0].lat), lon: parseFloat(data[0].lon) };
  } catch {
    return null;
  }
}
